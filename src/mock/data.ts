const generateEvidence = (count: number, uploader: 'consumer' | 'merchant') => {
  const types: Array<'image' | 'chat' | 'video' | 'document'> = ['image', 'chat', 'image', 'document'];
  return Array.from({ length: count }, (_, i) => ({
    id: `evi_${Date.now()}_${i}`,
    type: types[i % types.length],
    url: `https://picsum.photos/seed/evidence${i}${Date.now()}/400/300`,
    name: uploader === 'consumer' ? `证据图片${i + 1}.jpg` : `申诉凭证${i + 1}.jpg`,
    uploadTime: new Date(Date.now() - i * 3600000).toISOString(),
    uploader,
  }));
};

